def interpreter(tape):
    '''Creating interpreter.'''
    memory, step, output = {}, 0, ""
    for command in tape:
        if command == '>':
            step += 1
        elif command == '<':
            step -= 1
        elif command == '+':
            memory[step] = (memory.get(step, 0) + 1) % 256
        elif command == "*":
            output += chr(memory[step])
    return output

